function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) {
    return template;
  }

  return Object.entries(params).reduce((result, [key, value]) => result.replaceAll(`{${key}}`, String(value)), template);
}

export function localeTag(locale: LocaleCode) {
  switch (locale) {
    case "ru":
      return "ru-RU";
    case "kk":
      return "kk-KZ";
    default:
      return "en-US";
  }
}

export function useI18n() {
  const locale = useAuthStore((state) => state.locale);

  return {
    locale,
    localeTag: localeTag(locale),
    t: (key: string, fallback: string, params?: Record<string, string | number>) =>
      interpolate(translations[locale]?.[key] ?? fallback, params)
  };
}
